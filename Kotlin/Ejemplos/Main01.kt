package main.kotlin

// Kotlin generates bytecodes 100% compatible with Java
// You can say Kotlin is 'write Java in another way'
// The code is VERY similar to Java's, but with some exceptions

// This is an example of the Java's "public static void main(String[] args)"
// In Kotlin we define a function (fun) named main. It is NOT needed to pass
// arguments, but if you want to, you can change it for fun main(args : Array<String>)

// Also, note the ';' are not used in Kotlin

fun main() {
    println("Hello World!")
}