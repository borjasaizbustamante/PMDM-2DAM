package main.kotlin

// Kotlin Data Types are ALL CLASSES, unlike Java, who has primitive data types: int, long, etc.

fun main() {

    val myNum: Int = 5                // Int
    val myDoubleNum: Double = 5.99    // Double
    val myLetter: Char = 'D'          // Char
    val myBoolean: Boolean = true     // Boolean
    val myText: String = "Hello"      // String
    val isKotlinFun: Boolean = true   // Boolean

    println(myNum)
    println(myDoubleNum)
    println(myLetter)
    println(myBoolean)
    println(myText)
    println(isKotlinFun)
}